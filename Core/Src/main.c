// TOF pin configuration
static void VL53L0X_PROXIMITY_MspInit(void);

// Function declarations for VL53L0X init and range measurements
static void VL53L0X_PROXIMITY_Init(void);
static uint16_t VL53L0X_PROXIMITY_GetDistance(void);

int main(void)
{

	// HAL init and Clock config
  HAL_Init();
  SystemClock_Config();

  // Initialize all configured peripherals 
  MX_GPIO_Init();
  MX_USART1_UART_Init();
	
	// Initializes the TOF sensor (includes pin setup)
	VL53L0X_PROXIMITY_Init();
		
	// After testing the values from the sensor, they seemed off, and 
	// so after some experiments, it seemed the values returned from the sensor were about
	// 50mm off from the true value
	//
	// To compensate for this, 50mm are subtracted from all range values
	uint16_t calValue = 50;
	
	// Error message
	char errorMessage[] = "Error! Object out of range! \n\r";
  
  while (1)
  {
		
		// Value to hold the ranging data
		uint16_t prox_value = 0;
		
		// Char array to 
		char measureProx[40] = {0};
		
		// Get range distance
		prox_value = VL53L0X_PROXIMITY_GetDistance() - calValue;
		
		// use 2 meters as a cutoff; after this, it is unlikely that the values will be accurate 
		if (prox_value > 2000) {
		
			serialSend(errorMessage);
		
		}
		
		// Display metric (mm)
		else if (metricImperial == 0) {
		
			// Just format the measurement
			sprintf(measureProx, "Distance to Object: %d mm \n\r", prox_value);
		
		}
		
		// Assume inches 
		else {
		
			float newProx;
			// Convert from mm to inches
			newProx = prox_value/25.4;
			
			// Just format the measurement
			sprintf(measureProx, "Distance to Object: %.2f inches \n\r", newProx);
			
		}
		
		// Send the new string over serial
		serialSend(measureProx);
			
		// Delay for 1/10 of a second
		HAL_Delay(100);
		
  }

}

